package com.cg.dto;

public class WardenResponse {
	private Integer id;

	private String email;

	private String name;

	private Integer hostel_id;

	private String hostel_name;

	private String hostel_contact;

	private String hostel_type;

	private String hostel_address;

	private Double hostel_fee;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getHostel_id() {
		return hostel_id;
	}

	public void setHostel_id(Integer hostel_id) {
		this.hostel_id = hostel_id;
	}

	public String getHostel_name() {
		return hostel_name;
	}

	public void setHostel_name(String hostel_name) {
		this.hostel_name = hostel_name;
	}

	public String getHostel_contact() {
		return hostel_contact;
	}

	public void setHostel_contact(String hostel_contact) {
		this.hostel_contact = hostel_contact;
	}

	public String getHostel_type() {
		return hostel_type;
	}

	public void setHostel_type(String hostel_type) {
		this.hostel_type = hostel_type;
	}

	public String getHostel_address() {
		return hostel_address;
	}

	public void setHostel_address(String hostel_address) {
		this.hostel_address = hostel_address;
	}

	public Double getHostel_fee() {
		return hostel_fee;
	}

	public void setHostel_fee(Double hostel_fee) {
		this.hostel_fee = hostel_fee;
	}

}
